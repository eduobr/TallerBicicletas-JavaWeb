package clases;

import java.util.Date;

/**
 *
 * @author Zeit
 */
public class Cl_Venta {

    private int idVenta;
    private String nombreCompleto;
    private Date fecha;
    private String estado;
    private int cantidad;
    private int total;
    private int idTrabajador;
    private int idCliente;
    private String tipoPago;

    public Cl_Venta() {
    }

    public int getIdVenta() {
        return idVenta;
    }

    public void setIdVenta(int idVenta) {
        this.idVenta = idVenta;
    }

    public String getNombreCompleto() {
        return nombreCompleto;
    }

    public void setNombreCompleto(String nombreCompleto) {
        this.nombreCompleto = nombreCompleto;
    }

    public Date getFecha() {
        return fecha;
    }

    public void setFecha(Date fecha) {
        this.fecha = fecha;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public int getCantidad() {
        return cantidad;
    }

    public void setCantidad(int cantidad) {
        this.cantidad = cantidad;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public int getIdTrabajador() {
        return idTrabajador;
    }

    public void setIdTrabajador(int idTrabajador) {
        this.idTrabajador = idTrabajador;
    }

    public int getIdCliente() {
        return idCliente;
    }

    public void setIdCliente(int idCliente) {
        this.idCliente = idCliente;
    }

    public String getTipoPago() {
        return tipoPago;
    }

    public void setTipoPago(String tipoPago) {
        this.tipoPago = tipoPago;
    }

    @Override
    public String toString() {
        return "Cl_Venta{" + "idVenta=" + idVenta + ", nombreCompleto=" + nombreCompleto + ", fecha=" + fecha + ", estado=" + estado + ", cantidad=" + cantidad + ", total=" + total + ", idTrabajador=" + idTrabajador + ", idCliente=" + idCliente + ", tipoPago=" + tipoPago + '}';
    }

}
