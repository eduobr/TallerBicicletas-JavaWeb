package clases;

import java.util.Date;

/**
 *
 * @author Zeit
 */
public class Cl_Trabajador {

    private int idTrabajador;
    private String rut;
    private String nombre;
    private String cargo;
    private String direccion;
    private String correo;
    private Date fechaIngreso;
    private int sueldo;
    private String estado;
    private int idUsuario;

    public Cl_Trabajador() {
    }

    public int getIdTrabajador() {
        return idTrabajador;
    }

    public void setIdTrabajador(int idTrabajador) {
        this.idTrabajador = idTrabajador;
    }

    public String getRut() {
        return rut;
    }

    public void setRut(String rut) {
        this.rut = rut;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getCargo() {
        return cargo;
    }

    public void setCargo(String cargo) {
        this.cargo = cargo;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public Date getFechaIngreso() {
        return fechaIngreso;
    }

    public void setFechaIngreso(Date fechaIngreso) {
        this.fechaIngreso = fechaIngreso;
    }

    public int getSueldo() {
        return sueldo;
    }

    public void setSueldo(int sueldo) {
        this.sueldo = sueldo;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    @Override
    public String toString() {
        return "Cl_Trabajador{" + "idTrabajador=" + idTrabajador + ", rut=" + rut + ", nombre=" + nombre + ", cargo=" + cargo + ", direccion=" + direccion + ", correo=" + correo + ", fechaIngreso=" + fechaIngreso + ", sueldo=" + sueldo + ", estado=" + estado + '}';
    }

    public int getIdUsuario() {
        return idUsuario;
    }

    public void setIdUsuario(int idUsuario) {
        this.idUsuario = idUsuario;
    }

}
